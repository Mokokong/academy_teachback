package com.bptn.teachback.week1;

public class ComparisonOperators {

	
	public static void main(String[] args) {
		
		int a = 20;
		int b = 7;

		boolean result;

		
		result = a == b; 
		System.out.println("\na == b: "+ result);
		
		result = a != b; 
		System.out.println("\na != b: "+ result);
		
		result = a > b; 
		System.out.println("\na > b: "+ result);
		
		result = a < b; 
		System.out.println("\na < b: "+ result);
		
		result = a >= b; 
		System.out.println("\na >= b: "+ result);
		
		result = a <= b;
		System.out.println("\na <= b: "+ result);
	
	}
}
