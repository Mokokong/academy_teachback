package com.bptn.teachback.week1;

public class LogicalOperators {

	public static void main(String[] args) {
		
		boolean a = true; 
		boolean b = false; 

		
		System.out.println("\n a : "+ a);
		
		System.out.println("\n b : "+ b );
		boolean and = a && b; 
		boolean or = a || b;
		boolean not = !a; 
		
		System.out.println("\n a && b : "+ and);
		System.out.println("\n a || b : "+ or);
		System.out.println("\n !a : "+ not);
		
		
		
		

	}

}
